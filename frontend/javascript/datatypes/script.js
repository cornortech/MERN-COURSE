

/**
 * Data Types in JavaScript
 * 
 * 1. Primitive Data Types
 *  - Number - 10, 20.5, -5
 *  - String - "Hello, Ram!"
 *  - Boolean - true | false
 *  - Undefined - means a variable has been declared but not assigned a value
 *  - Null - empty object, intentional absence of any value 
 * 
 * 2. Reference Data Types
 * - Object
 * - Array
 */
// Variable Declaration and Assignment
let num = 10; // Number
let greeting = "Hello, Ram!"; // String
let isTodaySaturday  = false; // Boolean

// variable declaration

let username ; // Undefined
let whatIsMySirname = null; // Null

console.log(num , typeof num) ; // 10 number
console.log(greeting , typeof greeting); // Hello, Ram! string
console.log(isTodaySaturday , typeof isTodaySaturday); // false boolean
console.log(username , typeof username); // undefined undefined
console.log(whatIsMySirname , typeof whatIsMySirname); // null object



